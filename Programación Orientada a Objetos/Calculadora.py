class calcu :
    def suma(self, x, y):
        print ("El resultado de la suma es:" + str(x + y))
    
    def resta(self, x, y):
        print ("El resultado de la resta es:" + str(x -y))
    
    def multi(self, x, y):
        print ("El resultado de la multiplicacion es:" + str(x * y))
    
    def div(self, x, y):
        print ("El resultado de la division es:" + str(x / y))


